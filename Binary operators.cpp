//program to demonstarte the binary operator
#include <iostream>
using namespace std;

int main() 
{
  int a =8, b=3;
  
  //addition operator
  cout<<"a+b="<<(a+b)<<endl;
  
  //substraction operator
  cout<<"a-b="<<(a-b)<<endl;
  
  //multiplication operator
  cout<<"a*b="<<(a*b)<<endl;
  
  //divisiin operator
  cout<<"a/b="<<(a/b)<<endl;
  
  //modulo operator
  cout<<"a%b="<<(a%b)<<endl;
  
  return 0;
}